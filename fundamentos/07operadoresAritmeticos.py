#los operadores aritmeticos son aquellos que nos sirven para realizar operaciones
#matematicas estos serian: (*+-/%//)
#sumar se utiliza con  el signo de mas (+)
print(20 + 20)
#resta se utiliza con el signo de menos (-)
print (20 - 10)
#multiplicacion con el simbolo asterisco (*)
print(5 * 5)
#division se usa el slash (/)
#dividir en cero dara error! cuidado
print (10 / 2)
#para divir y recibir solo numero enteros se usa el doble slash(//)
print (10 // 2)
#el exponencial (**)
print (2 **5 )
#para conocer el restante de una division se usa (%)
print (23 % 3)

