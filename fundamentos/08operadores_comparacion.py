'''
los operadores de comparacion nos ayudan con la logica, estos como resultado
nos daran tanto verdadero (TRUE) como falso (FALSE)
estos deben escribirse con sus primeras letras en mayuscula
'''
# > mayor que
print(7 > 6)
print(23 > 5)

# < menor que
print(98 < 101)
print(32 < 16)

# == comparacion o igualdad
print(10 == 10)
print(11 == 99)
print( "angel" == "angel")
print (1 == "1")

# != no es igual
print(10 != 10)
print(12 != 3)

# >= mayor o igual a 
print(2 >= 3)
print(3 >= 3)

# <= menor o igual a
print(23 <= 12)
print(23 <= 23)

#podemos usar esos operadores tambien con variables
edad = 18
mayor = 18
edad_3 = 2
print(edad > mayor)
print(edad_3 > mayor)
